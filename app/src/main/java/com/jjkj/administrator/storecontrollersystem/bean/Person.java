package com.jjkj.administrator.storecontrollersystem.bean;

/**
 * @author Administrator
 */

public class Person {

    private Object achievement;
    private String age;
    private Object basePay;
    private Object entryDate;
    private int id;
    private String meId;
    private String name;
    private String phone;
    private int picture;

    public Object getAchievement() {
        return achievement;
    }

    public void setAchievement(Object achievement) {
        this.achievement = achievement;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public Object getBasePay() {
        return basePay;
    }

    public void setBasePay(Object basePay) {
        this.basePay = basePay;
    }

    public Object getEntryDate() {
        return entryDate;
    }

    public void setEntryDate(Object entryDate) {
        this.entryDate = entryDate;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getMeId() {
        return meId;
    }

    public void setMeId(String meId) {
        this.meId = meId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public int getPicture() {
        return picture;
    }

    public void setPicture(int picture) {
        this.picture = picture;
    }
}
